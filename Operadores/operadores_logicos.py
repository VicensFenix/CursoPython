# AND (Devuelve True si ambas condiciones se cumplen)

resultado = True and True  # Devolver Verdadero
resultado2 = False and True  # Devolver Falso
resultado3 = True and False  # Devolver Falso
resultado4 = False and False  # Devolver Falso

# OR (Devuelve True si al menos una condición se cumple)

resultado5 = True or True  # Devolver Verdadero
resultado6 = False or True  # Devolver Verdadero
resultado7 = True or False  # Devolver Verdadero
resultado8 = False or False  # Devolver Falso

# NOT (Niega la condición, invierte el valor)

resultado9 = not True  # Devolver Falso
resultado10 = not False  # Devolver Verdadero

print(resultado10)