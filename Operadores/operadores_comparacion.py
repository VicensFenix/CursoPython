# == es igual que
# (!=) es distinto que (Ignoren el parentesis del signo)
# < es menor que
# <= es menor o igual que
# > es mayor que
# >= es mayor o igual que 

# Verificar si ambos valores asignados son iguales
es_igual_que = 5 == 5

# Verifica si ambos valores asignados son distintos
es_distinto_que = 6 != 5 

# Verifica que el primer valor sea menor que el segundo valor
es_menor_que = 5 < 6

# Verifica que el primer valor sea menor o igual que el segundo valor
menor_o_igual = 2 <= 2

# Verifica que el primer valor sea mayor que el segundo valor
es_mayor_que = 7 > 6

# Verifica que el primer valor sea mayor o igual que el segundo valor
mayor_o_igual = 5 >= 2


# Mostramos en pantalla
print(es_igual_que)
print(es_distinto_que)
print(es_menor_que)
print(menor_o_igual)
print(es_mayor_que)
print(mayor_o_igual)

