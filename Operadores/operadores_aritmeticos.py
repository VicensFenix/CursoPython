# Suma(+)  a + b = 15
# Resta(-)  b - a = 5
# Multiplicación(*)  a * b = 50
# División(/)  b / a = 2
# Modulo-Devuelve el resto de la división(%)  b % a = 0
# Exponente-Realiza exponencial(**)  b ** a = 10000
# División baja-Devuelve el entero de la división(//)  b // a = 2


# Suma y resta (+ y -)
suma = 12 + 5
resta = 12 - 5

# Multiplicación y división (* y /)
multiplicacion = 12 * 5
division = 12 / 5 # Devuelve un lado float (flotante)

# Potenciación (exponente) (**)
exponente = 12 ** 2

# División baja (//)
division_baja = 12 // 5 # Devuelve entero rendoneado hacia abajo

# Resto o módulo
modulo = 12 % 5

# Type sirve para indicar que tipo de dato es
tipo_de_dato = type(division_baja) 
print(tipo_de_dato)

# Imprimir en pantalla
print(suma)
print(resta)
print(multiplicacion)
print(division)
print(exponente)
print(division_baja)
print(modulo)


